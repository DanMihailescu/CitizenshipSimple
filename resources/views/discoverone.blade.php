                         ouicards.bucketC.length ? ouicards.bucketC : [];

    ouicards.counter = 1;
    return {flashcards: ouicards.flashcards, bucketA: ouicards.bucketA, bucketB: ouicards.bucketB, bucketC: ouicards.bucketC};

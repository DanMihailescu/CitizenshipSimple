        <div class="question1a">
            <h4 class="dark-grey-text my-3 py-3">Are you Eligible to Apply for Citizenship?</h4>
            <p>In order to become eligible to become a Canadian citizen, you must:</p>
            <ul class="list-group py-3 my-3">
                <li class="list-group-item"><p>be a permenant resident</p></li>
                <li class="list-group-item"><p>have lived in Canada for 3 out of the last 5 years</p></li>
                <li class="list-group-item"><p>have filed your taxes, if you need to</p></li>
                <li class="list-group-item"><p>pass a test on your rights, responsibilities and knowledge of Canada(Citizenship test)</p></li>
                <li class="list-group-item"><p>prove your language skills</p></li>
            </ul class="list-group py-3 my-3">
    
            <p>There are additional or different requirements if you are:</p>
            <ul class="list-group py-3 my-3">
                <li class="list-group-item"><p>applying for a minor (under age 18)</p></li>
                <li class="list-group-item"><p>a Canadian applying for your adopted child born outside Canada</p></li>
                <li class="list-group-item"><p>a current or former Canadian Armed Forces (CAF) member applying under the fast-track process</p></li>
                <li class="list-group-item"><p>a past Canadian citizen who want your Canadian citizenship back (including current and former CAF members)</p></li>
            </ul class="list-group py-3 my-3">
        </div>
    
        <div class="question2a">
            <h4 class="dark-grey-text my-3 py-3">Do you Automatically become a citizen if you're a spouse of Canadian citizen?</h4 class="dark-grey-text my-3 py-3">
            <p>You don’t automatically become a citizen when you marry a Canadian.</p>
            <p>If you’re the spouse of a Canadian citizen, you must meet the same requirements listed above (no exception).</p>
        </div>
    
        <div class="question3a">
            <h4 class="dark-grey-text my-3 py-3">If you're a child or grandchild of Canadian citizens, can you become a citizen?</h4 class="dark-grey-text my-3 py-3">
            <p>If you have a Canadian parent or grandparent, you may be a Canadian citizen.</p>
        </div>
        
        <div class="question4a">
            <h4 class="dark-grey-text my-3 py-3">What is permanent resident status and is it required?</h4 class="dark-grey-text my-3 py-3">
            <p>Regardless of your age, if you’re applying for citizenship, you must have permanent resident (PR) status in Canada.</p>
            <p>This means you must <b>not</b>:</p>
            <ul class="list-group py-3 my-3">
                <li class="list-group-item"><p>be under review for immigration or fraud reasons</p></li>
                <li class="list-group-item"><p>be asked by Canadian officials to leave Canada (removal order)</p></li>
                <li class="list-group-item"><p>have unful filled conditions related to your PR status, for example: medical screening</p></li>
            </ul class="list-group py-3 my-3">
            <p>Before applying for citizenship, you should review the documents you received when you became a permanent resident to make sure you’re eligible.</p>
        </div>
        
        <div class="question5a">
            <h4 class="dark-grey-text my-3 py-3">In order to apply for Citizenship; do you need to have a valid permenant resident card?</h4 class="dark-grey-text my-3 py-3">
            <p>You don’t need a valid PR card to apply for citizenship. You can apply with an expired PR card.</p>
        </div>
        
        <div class="question6a">
            <h4 class="dark-grey-text my-3 py-3">How many days within the five years must you have lived in Canada?</h4 class="dark-grey-text my-3 py-3">
            <p>Adults and some minors must have been physically present in Canada for at least 1095 days during the five years right before the date you sign your application.</p>
            <p>We encourage you to apply with more than 1095 days of physical presence to have extra days in case there is a problem with the calculation.</p>
        </div>
    
        <div class="question7a">
            <h4 class="dark-grey-text my-3 py-3">How many years of taxes need to be filed?</h4 class="dark-grey-text my-3 py-3">
            <p>You need to have filed taxes in Canada for at least 3 years during the 5 years right before the date you apply, if you needed to.</p>
        </div>
    
        <div class="question8a">
            <h4 class="dark-grey-text my-3 py-3">How old do you need to be to not have to show your language skills?</h4 class="dark-grey-text my-3 py-3">
            <p>Canada has two official languages: English and French. If you’re <b>NOT</b> 18 to 54 years of age on the day you sign your application, you dont have to show that you can speak and listen at a specific level in one of these languages.</p>
        </div>
    
        <div class="question9a">
            <h4 class="dark-grey-text my-3 py-3">In what ways are your language skills measured?</h4 class="dark-grey-text my-3 py-3">
            <p>The ways we measure your language skills in English or French include:</p>
            <ul class="list-group py-3 my-3">
                <li class="list-group-item"><p>reviewing the proof you send with your application</p></li>
                <li class="list-group-item"><p>noting how well you communicate when you talk to a citizenship official anytime during the process</p></li>
                <li class="list-group-item"><p>assessing your language level during a hearing with a citizenship official, if necessary</p></li>
            </ul class="list-group py-3 my-3">
        </div>
        
        <div class="question10a">
            <h4 class="dark-grey-text my-3 py-3">What language benchmark is needed to become a citizen?</h4 class="dark-grey-text my-3 py-3">
            <p>To become a citizen, you need to meet the Canadian Language Benchmarks (CLB) Level 4 or higher. This means you can:</p>
            <ul class="list-group py-3 my-3">
                <li class="list-group-item"><p>take part in short, everyday conversations about common topics</p></li>
                <li class="list-group-item"><p>understand simple instructions, questions and directions</p></li>
                <li class="list-group-item"><p>use basic grammar, including simple structures and tenses</p></li>
                <li class="list-group-item"><p>show you know enough common words and phrases to answer questions and express yourself</p></li>
            </ul class="list-group py-3 my-3">
            <p>We accept various certificates, diplomas and tests as proof of your language skills.</p>
        </div>
        
        <div class="question11a">
            <h4 class="dark-grey-text my-3 py-3">What age is it mandatory to take the Candian citizenship test?</h4 class="dark-grey-text my-3 py-3">
            <p>If you’re 18 to 54 years of age on the day you sign your application, you need to take the citizenship test.</p>
        </div>
        
        <div class="question12a">
            <h4 class="dark-grey-text my-3 py-3">What are some restrictions for becoming a citizen?</h4 class="dark-grey-text my-3 py-3">
            <p>If you committed a crime in or outside Canada, you may not be eligible to become a Canadian citizen for a period of time.</p>
            <p>Time spent serving a term of imprisonment, on parole, or on probation doesn’t count as time you have lived in Canada.</p>
            <p>Some of these restrictions include: </p>
            <ul class="list-group py-3 my-3">
                <li class="list-group-item">
                    <p>you're in Canada</p>
                    <ul class="list-group py-3 my-3">
                        <li class="list-group-item"><p>serving a term of imprisonment</p></li>
                        <li class="list-group-item"><p>on parole</p></li>
                        <li class="list-group-item"><p>on probation</p></li>
                    </ul class="list-group py-3 my-3">
                </li>
                <li class="list-group-item"><p>you’re serving a sentence outside Canada</p></li>
                <li class="list-group-item"><p>you’re charged with, on trial for, or involved in an appeal for an offence</p>
                    <ul class="list-group py-3 my-3">
                        <li class="list-group-item"><p>under the <em>Citizenship Act</em>, or an indictable offence in Canada</p></li>
                        <li class="list-group-item"><p>committed outside Canada that’s equivalent to an indictable offence in Canada</p></li>
                    </ul class="list-group py-3 my-3">
                </li>
                <li class="list-group-item"><p>you’re under a removal order (Canadian officials asked you to leave Canada)</p></li>
                <li class="list-group-item">
                    <p>you’re being investigated for, are charged with, on trial for, involved in an appeal for or have been convicted of</p>
                    <ul class="list-group py-3 my-3">
                        <li class="list-group-item"><p>a war crime, or</p></li>
                        <li class="list-group-item"><p>a crime against humanity</p></li>
                    </ul class="list-group py-3 my-3">
                </li>
                <li class="list-group-item"><p>you had a citizenship application refused for misrepresentation in the past 5 years</p></li>
                <li class="list-group-item"><p>you had your Canadian citizenship revoked (taken away) because of fraud in the past 10 years</p></li>
                <li class="list-group-item">
                    <p>you’ve been convicted of an indictable offence in Canada or an offence under the Citizenship Act, and</p>
                    <li class="list-group-item">if we received your application after June 11, 2015, and this conviction took place in the 4 years before you apply</li>
                </li>
                <li class="list-group-item">
                    <p>in the 4 years before you apply, you were convicted of an offence outside Canada that’s equivalent to an indictable offence in Canada. This applies</p>
                    <ul class="list-group py-3 my-3">
                        <li class="list-group-item"><p>even if you were pardoned or granted amnesty</p></li>
                        <li class="list-group-item"><p>regardless of when we receive your application</p></li>
                    </ul class="list-group py-3 my-3">
                </li>
                <li class="list-group-item">
                    <p>while a permanent resident, you</p>
                    <ul class="list-group py-3 my-3">
                        <li class="list-group-item"><p>were convicted of terrorism, high treason, treason or spying offences</p></li>
                        <li class="list-group-item"><p>served as a member of an armed force of a country or territory, or an organized armed group, that’s engaged in armed conflict with Canada</p></li>
                    </ul class="list-group py-3 my-3">
                </li>
            </ul class="list-group py-3 my-3">
            <br><br>
            <p>If any of the above situations apply to you</p>
            <ul class="list-group py-3 my-3">
                <li class="list-group-item">
                    <p>wait until the situation no longer applies before you apply for citizenship</p>
                    <li class="list-group-item">Exception: You can never resume your citizenship if it was revoked due to fraud in the past 10 years.</li>
                </li>
                <li class="list-group-item"><p>tell us about the situation in your application</p></li>
                <li class="list-group-item"><p>we’ll review your application on a case-by-case basis</p></li>
            </ul class="list-group py-3 my-3">
        </div>
